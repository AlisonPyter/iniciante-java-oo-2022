
public abstract class ColaboradoresMovimentoCodar{
	
	private String nome;
	private String cpf;
	public String funcao;
	
	public ColaboradoresMovimentoCodar() {
	
	
	}
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCpf() {
		return cpf;
	}
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
	public String getFuncao() {
		return funcao;
	}
	public void setFuncao(String funcao) {
		this.funcao = funcao;
	}
	
	
	

}
